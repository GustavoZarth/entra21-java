
public class Exemplos {
	public static void main(String[] args) {

		int numero = 20;
		
		while (numero >=  10) {
			System.out.println(numero);
			numero--;
		}
		
		System.out.println("----------------------------------------------");
		
		for (int i = 20; i >= 10; i--) {
			System.out.println(i);
		}
		
	}
}
